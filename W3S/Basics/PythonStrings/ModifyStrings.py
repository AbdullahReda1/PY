# Some function that used to modify strings
# INitializing a text string
txt = " Hello World"

print(txt.upper()) # Convert to uppercase

print(txt.lower()) # Convert to lowercase

print(txt.strip()) # Remove whitespace from the beginning or the end

print(txt.replace("H", "J"))    # Replace a character with another character

print(txt.split())  # Split the string into a list where each word is a list item